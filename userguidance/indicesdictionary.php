		  Definitions and mathematical formulas of the indices used in ECA&amp;D are provided below. A core set of 26 indices follows the definitions recommended by the CCl/CLIVAR/JCOMM Expert Team on Climate Change Detection and Indices (<a href="http://www.clivar.org/organization/etccdi/etccdi.php" target="_blank">ETCCDI</a>). Another 46 indices are specifically for Europe. Note that new research may lead to additional indices or changes in the indices definitions in the future.<br><br>

		  The station series used for indices calculation in ECA&amp;D are <strong>blended</strong> series. This means that they are made near-complete by infilling from nearby stations and updated using synoptical messages. Details on the blend and update proces are given in the <a href="http://www.ecad.eu/FAQ/index.php#3" target="_top">specific FAQ</a> or in the <a href="http://www.ecad.eu/documents/atbd.pdf" target="_blank">ATBD</a>.<br><br>

